CREATE TABLE TeamsInGroup(
	ID INT PRIMARY KEY IDENTITY(1, 1),
	TeamIdInGroup INT NOT NULL,
	GroupsFK INT NOT NULL,
	TeamsFK INT NOT NULL,
	FOREIGN KEY(GroupsFK) REFERENCES Groups(ID),
	FOREIGN KEY(TeamsFK) REFERENCES Teams(ID),
);
GO

IF EXISTS (SELECT * FROM sys.objects WHERE name = 'InputTeamsInGroups' AND type = 'P')
    DROP PROCEDURE InputTeamsInGroups;
GO

CREATE PROCEDURE InputTeamsInGroups 
AS
BEGIN
	DECLARE @groupId INT;
	DECLARE @teamId INT;
	DECLARE @mno INT;
	DECLARE @teamIdInGroup INT;
	SET @groupId = 1;
	SET @teamId = 1;
	SET @mno = 0;
	SET @teamIdInGroup = 1;

	WHILE @groupId <= 8
	BEGIN
	WHILE @teamId <= 4
	BEGIN
		INSERT INTO TeamsInGroup(TeamIdInGroup, GroupsFK, TeamsFK)VALUES (@teamIdInGroup, @groupId, @teamId + (@mno * 4));
		SET @teamId = @teamId + 1;
		SET @teamIdInGroup = @teamIdInGroup + 1;
	END
	SET @teamIdInGroup = 1;
	SET @teamId = 1;
	SET @mno = @mno + 1;
	SET @groupId = @groupId + 1;
	END
END;
GO

EXEC InputTeamsInGroups
GO

